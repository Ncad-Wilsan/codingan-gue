using namespace std;
/// @brief 
/// @return 
int main() {
    // Integer data types
    int a = 10;
    short b = 20;
    long c = 30;
    long long d = 40;

    // Floating-point data types
    float e = 1.23f;
    double f = 2.34;
    long double g = 3.45;

    // Character data types
    char h = 'A';
    wchar_t i = L'B';
    char16_t j = u'C';
    char32_t k = U'D';

    // Boolean data type
    bool l = true;

    // String data type
    string m = "Hello, World!";

    // Output
    cout << "Integer: " << a << endl;
    cout << "Short: " << b << endl;
    cout << "Long: " << c << endl;
    cout << "Long Long: " << d << endl;
    cout << "Float: " << e << endl;
    cout << "Double: " << f << endl;
    cout << "Long Double: " << g << endl;
    cout << "Char: " << h << endl;
    cout << "Wchar_t: " << i << endl;
    cout << "Char16_t: " << j << endl;
    cout << "Char32_t: " << k << endl;
    cout << "Bool: " << l << endl;
    cout << "String: " << m << endl;

    return 0;
}
